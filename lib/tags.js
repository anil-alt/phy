'use strict';

function hhTags(h, tag, selector, attrs, kids) {
  if ('string' === typeof selector) {
    if (!/^[.#]/.test(selector))
      throw new Error(`${tag}(): string children must be passed in an array`);
    return h(`${tag}${selector}`, attrs, kids);
  }
  // no selector given, pass next two args shifted
  return h(tag, selector, attrs);
}

const h = require('../');

const hh = hhTags.bind(null, h);

const TAG_NAMES =
  'a|abbr|address|area|article|aside|audio|b|base|bdi|bdo|big|blockquote|body|br|button|canvas|caption|cite|code|col|colgroup|data|datalist|dd|del|details|dfn|dialog|div|dl|dt|em|embed|fieldset|figcaption|figure|footer|form|h1|h2|h3|h4|h5|h6|head|header|hgroup|hr|html|i|iframe|img|input|ins|kbd|keygen|label|legend|li|link|main|map|mark|menu|menuitem|meta|meter|nav|noscript|object|ol|optgroup|option|output|p|param|picture|pre|progress|q|rp|rt|ruby|s|samp|script|section|select|small|source|span|strong|style|sub|summary|sup|table|tbody|td|textarea|tfoot|th|thead|time|title|tr|track|u|ul|video|wbr|circle|clipPath|defs|ellipse|g|image|line|linearGradient|mask|path|pattern|polygon|polyline|radialGradient|rect|stop|svg|text|tspan';

exports.h = h;

TAG_NAMES.split('|').forEach(tag => {
  exports[tag] = hh.bind(null, tag);
});
